class P9
{
	public static void main(String args[])
	{
      for(int i=1;i<=5;i++)
	  { 
         for(int k=1;k<i;k++)
		  {
			System.out.print(" ");  
		  }
         
		  for(int j=i;j<=5;j++)
		  {
			System.out.print(j+" ");  
		  }
		  System.out.println();
	    }
    for(int i=4;i>0;i--)
	  { 
         for(int k=i;k>1;k--)
		  {
			System.out.print(" ");  
		  }
         
		  for(int j=i;j<=5;j++)
		  {
			System.out.print(j+" ");  
		  }
		  System.out.println();
	    }
		
		  
	  
	}		
}
